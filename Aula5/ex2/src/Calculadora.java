public class Calculadora {
    public double somar(double p_num1, double p_num2)
    {
        return p_num1 + p_num2;
    }

    public double subtrair(double p_num1, double p_num2)
    {
        return p_num1 - p_num2;
    }

    public double multiplicar(double p_num1, double p_num2)
    {
        return p_num1 * p_num2;
    }

    public double dividir(double p_num1, double p_num2)
    {
        return  p_num1 / p_num2;
    }
}
