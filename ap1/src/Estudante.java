public class Estudante
{
    private String nome;
    private int idade;
    private int pontos;

    public String getNome()
    {
        return this.nome;
    }

    public void setNome(String p_nome)
    {
        this.nome = p_nome;
    }

    public int getIdade()
    {
        return this.idade;
    }

    public void setIdade(int p_idade)
    {
        this.idade = p_idade;
    }

    public int getPontos()
    {
        return this.pontos;
    }

    public void setPontos(int p_pontos)
    {
        this.pontos = p_pontos;
    }
}
